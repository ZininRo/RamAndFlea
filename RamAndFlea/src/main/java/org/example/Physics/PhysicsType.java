package org.example.Physics;
/**
 * Перечисление типов физических объектов в игровом мире.
 * Используется для идентификации тел в обработчиках коллизий и отрисовке.
 */
public enum PhysicsType {
    FLEA, // Снаряд (блоха)
    RAM, // Баран (цель)
    BLOCK, // Блок конструкции
    GROUND, // Пол (статическая граница)
    WALL, // Стена (статическая граница)
    CEILING // Потолок (статическая граница)
}